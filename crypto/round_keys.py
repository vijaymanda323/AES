# AES round constants (Rcon) — 10 values for AES-128 key schedule.
_RCON: list[int] = [
    0x01, 0x02, 0x04, 0x08, 0x10,
    0x20, 0x40, 0x80, 0x1B, 0x36,
]


def _sub_word(word: int) -> int:
    """Apply S-Box substitution to each byte of a 32-bit word."""
    return (
        (_SBOX[(word >> 24) & 0xFF] << 24)
        | (_SBOX[(word >> 16) & 0xFF] << 16)
        | (_SBOX[(word >> 8)  & 0xFF] << 8)
        |  _SBOX[ word        & 0xFF]
    )


def _rot_word(word: int) -> int:
    """Rotate a 32-bit word left by one byte (8 bits)."""
    return ((word << 8) | (word >> 24)) & 0xFFFFFFFF


def _bytes_to_word(b: bytes, offset: int) -> int:
    """Read 4 bytes at *offset* from *b* as a big-endian 32-bit integer."""
    return int.from_bytes(b[offset : offset + 4], "big")


def _word_to_bytes(word: int) -> bytes:
    """Convert a 32-bit integer to 4 bytes (big-endian)."""
    return word.to_bytes(4, "big")


def get_round_keys(key_hex: str) -> list[str]:
    """Expand a 128-bit AES key into 11 round keys using the AES-128 key schedule.

    Args:
        key_hex: 32-character hexadecimal string (16 bytes / 128-bit key).

    Returns:
        A list of 11 hex strings, each 32 characters long (16 bytes).
        Index 0 is the original key; indices 1-10 are the expanded round keys.

    Raises:
        ValueError: If *key_hex* is not a valid 32-character hex string.
    """
    if len(key_hex) != 32:
        raise ValueError("key_hex must be exactly 32 hexadecimal characters.")
    try:
        key_bytes: bytes = bytes.fromhex(key_hex)
    except ValueError as exc:
        raise ValueError("key_hex contains invalid hexadecimal characters.") from exc

    # Number of 32-bit words in the key (Nk = 4 for AES-128)
    Nk = 4
    # Total words needed: 4 words × 11 rounds = 44
    Nr = 10
    total_words = Nk * (Nr + 1)

    # Initialise W[0..3] from the original key
    W: list[int] = [_bytes_to_word(key_bytes, i * 4) for i in range(Nk)]

    for i in range(Nk, total_words):
        temp: int = W[i - 1]
        if i % Nk == 0:
            temp = _sub_word(_rot_word(temp)) ^ (_RCON[i // Nk - 1] << 24)
        W.append(W[i - Nk] ^ temp)

    # Group words into 11 round keys (4 words = 16 bytes each)
    round_keys: list[str] = []
    for r in range(Nr + 1):
        rk_bytes = b"".join(_word_to_bytes(W[r * 4 + j]) for j in range(4))
        round_keys.append(rk_bytes.hex())

    return round_keys
