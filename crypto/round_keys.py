"""
round_keys.py — AES-128 Key Schedule Expansion
------------------------------------------------
Implements the AES-128 key expansion algorithm (FIPS 197, Section 5.2) in
pure Python using `pycryptodome`'s internal S-Box table for SubWord.

Produces 11 round keys (rounds 0 through 10):
    - Round 0  : the original 128-bit key
    - Rounds 1-10 : expanded keys derived from the previous round key

Public API
~~~~~~~~~~
    get_round_keys(key_hex) → list[str]
        Returns 11 hex strings, each 32 hex characters (16 bytes).
"""

